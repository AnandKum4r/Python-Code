# 2.Relational Operators
# It is used for compare
# Gives output in true/false
# *imp Boolean data types by default value is false 
x=50
y=10
print(x>y) #Greater than
print(x<y) #Less than
print(x<=y) #less than or equal to
print(x>=y) #greater than or eqaul to
print(x==y) #equal to
print(x!=y) #not equal to